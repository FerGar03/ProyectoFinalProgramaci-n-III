    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        //Guarda los datos en la tabla "stock". Verifica si ya hay un registro con el mismo "nombre", si es asi, actualiza el campo
        //"cantidad" sumando los datos que ya estan m√°s los del nuevo registro. Si no hay registro con el mismo "nombre"
        //simplemente se crea un nuevo registro
        cls_conexion mysql= new cls_conexion();
        Connection cn=mysql.getConnection();
        
        String producto = (String) combo_productos.getSelectedItem();
        int cantidad = Integer.parseInt(jtextcantidad.getText());
        int idstock;
        id_stock s =new id_stock();
        idstock= s.id_incrementablestock();
        try{
            String queryVerificar = "SELECT cantidad FROM stock WHERE producto = ?";
            PreparedStatement statementVerificar = cn.prepareStatement(queryVerificar);
            statementVerificar.setString(1, producto);
            
            ResultSet resultSet = statementVerificar.executeQuery();
            
            if (resultSet.next()) {
                // El producto ya existe, se debe actualizar la cantidad
                int cantidadExistente = resultSet.getInt("cantidad");
                int nuevaCantidad = cantidadExistente + cantidad;
                
                String queryActualizar = "UPDATE stock SET cantidad = ? WHERE producto = ?";
                PreparedStatement statementActualizar = cn.prepareStatement(queryActualizar);
                statementActualizar.setInt(1, nuevaCantidad);
                statementActualizar.setString(2, producto);
                
                statementActualizar.executeUpdate();
                
                JOptionPane.showMessageDialog(null,"Cantidad actualizada en la tabla 'stock'.","Ingreso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // El producto no existe, se inserta como un nuevo registro
                String queryInsertar = "INSERT INTO stock (idstock, producto, cantidad) VALUES (?, ?, ?)";
                PreparedStatement statementInsertar = cn.prepareStatement(queryInsertar);
                statementInsertar.setInt(1, idstock);
                statementInsertar.setString(2, producto);
                statementInsertar.setInt(3, cantidad);
                        
                statementInsertar.executeUpdate();
               
                JOptionPane.showMessageDialog(null,"Datos insertados correctamente en la tabla 'stock'.","Ingreso", JOptionPane.INFORMATION_MESSAGE);
               
            }
        }catch(SQLException e){
        
             JOptionPane.showMessageDialog(null,"Error al insertar/actualizar datos en la tabla 'stock': "+e,"Ingreso", JOptionPane.ERROR_MESSAGE);
             
             
        }
        
 //limpiar casillas
        
             
        jtextcantidad.setText("");
         combo_productos.setSelectedIndex(0); //posicion cero es la primera
         
        
        

    }//GEN-LAST:event_btnguardarActionPerformed

